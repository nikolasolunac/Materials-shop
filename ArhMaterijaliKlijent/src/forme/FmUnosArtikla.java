    private void srediFormuArtikalNijeKreiran() {
        daLiJeKreiran = false;
        jtxtNaziv.setEnabled(false);
        jtxtCena.setEnabled(false);
        jtxtaOpis.setEnabled(false);
        jtxtJedinicaMere.setEnabled(false);
        jcmbTipArtikla.setEnabled(false);
        jbtnOdustani.setEnabled(false);
        jbtnSacuvajArtikal.setEnabled(false);
        jbtnKreirajArtikal.setEnabled(true);

    }

    private void prikaziNoviArtikal(Artikal artikal) {
        jtxtArtikalID.setText(artikal.getArtikalID() + "");
    }

    private void srediFormuArtikalJeKreiran() {
        jbtnKreirajArtikal.setEnabled(false);
        jtxtNaziv.setEnabled(true);
        jtxtCena.setEnabled(true);
        jtxtaOpis.setEnabled(true);
        jtxtJedinicaMere.setEnabled(true);
        jcmbTipArtikla.setEnabled(true);
        jbtnOdustani.setEnabled(true);
        jbtnSacuvajArtikal.setEnabled(true);

    }

    private void prikaziPorukuObavestenja(String poruka) {
        JOptionPane.showMessageDialog(this, poruka, "Obave≈°tenje!", JOptionPane.INFORMATION_MESSAGE);
    }

    private void popuniComboTipoviArtikla(List<OpstiDomenskiObjekat> lista1) {
        jcmbTipArtikla.removeAllItems();
        jcmbTipArtikla.setModel(new DefaultComboBoxModel(lista1.toArray()));
    }

    private void obrisiPodatkeIzPolja() {
        jtxtNaziv.setText("");
        jtxtCena.setText("");
        jtxtaOpis.setText("");
        jtxtJedinicaMere.setText("");
        jcmbTipArtikla.setSelectedIndex(0);
    }

    private void prikaziPodatkeArtikla(Artikal selektovanArtikal) {
        jtxtArtikalID.setText(selektovanArtikal.getArtikalID() + "");
        jtxtNaziv.setText(selektovanArtikal.getNaziv());
        jtxtCena.setText(selektovanArtikal.getCena() + "");
        jtxtaOpis.setText(selektovanArtikal.getOpis());
        jtxtJedinicaMere.setText(selektovanArtikal.getJedinicaMere());
        jcmbTipArtikla.setSelectedItem(selektovanArtikal.getTipArtikla());
    }

