    private void jbtnOdustaniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnOdustaniActionPerformed
        // TODO add your handling code here:
        if (daLiJeKreiran) {
            try {
                kontroler.KontrolerKlijent.getInstance().obrisiArtikal(jtxtArtikalID.getText() + "");
                daLiJeKreiran = false;
                srediFormuArtikalNijeKreiran();
                obrisiPodatkeIzPolja();
            } catch (Exception e) {
                prikaziPorukuGreske("Gre≈°ka prilikom brisanja kreiranog artikla!");
            }
        }
    }//GEN-LAST:event_jbtnOdustaniActionPerformed

    private void jbtnSacuvajArtikalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajArtikalActionPerformed
        // TODO add your handling code here:
        try {
            double cena;
            int artikalID = Integer.parseInt(jtxtArtikalID.getText().trim());
            String naziv = jtxtNaziv.getText().trim();
            String opis = jtxtaOpis.getText().trim();
            String jedinicaMere = jtxtJedinicaMere.getText().trim();
            TipArtikla tipArtikla = (TipArtikla) jcmbTipArtikla.getSelectedItem();

            if (naziv == null || naziv.isEmpty()) {

                prikaziPorukuGreske("Niste uneli naziv artikla!");
                return;
            }

            try {
                cena = Double.parseDouble(jtxtCena.getText().trim());
                if (!(cena > 0)) {
                    prikaziPorukuGreske("Niste ispravno uneli cenu!");
                    return;
                }
            } catch (Exception e) {
                prikaziPorukuGreske("Niste uneli cenu!");
                return;
            }

            Artikal artikal = new Artikal(artikalID, naziv, cena, opis, jedinicaMere, tipArtikla);

            if (kontroler.KontrolerKlijent.getInstance().getAktivan_sk() == util.Util.SK_UNOS) {
                kontroler.KontrolerKlijent.getInstance().zapamtiArtikal(artikal);
                prikaziPorukuObavestenja("Ssitem je saƒçuvao artikal!");
                srediFormuArtikalNijeKreiran();
                obrisiPodatkeIzPolja();
            }
            

        } catch (Exception ex) {
            if (ex.getMessage() != null && ex.getMessage().equals(util.Util.VEC_POSTOJI_PORUKA)) {
                prikaziPorukuGreske("Veƒá je unet ovakav artikal!");
                return;
            }
            ex.printStackTrace();
            prikaziPorukuGreske("Sistem ne mo≈æe da zapamti artikal!");
        }
    }//GEN-LAST:event_jbtnSacuvajArtikalActionPerformed

