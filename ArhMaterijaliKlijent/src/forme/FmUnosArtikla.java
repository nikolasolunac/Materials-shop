    /**
     * @param args the command line arguments
     */
    
    private void srediFormuPremaSK() {
        int aktivan = kontroler.KontrolerKlijent.getInstance().getAktivan_sk();
        if (aktivan == util.Util.SK_UNOS) {
            srediFormuArtikalNijeKreiran();
            this.setTitle("Unos podataka artikla");

        }
        
        if(aktivan == util.Util.SK_PRIKAZ) {
           jtxtArtikalID.setEditable(false);
           jtxtNaziv.setEditable(false);
           jtxtCena.setEditable(false);
           jtxtaOpis.setEditable(false);
           jtxtJedinicaMere.setEditable(false);
           jbtnKreirajArtikal.setEnabled(false);
           jbtnOdustani.setEnabled(false);
           jbtnSacuvajArtikal.setEnabled(false);
           jcmbTipArtikla.setEditable(false);
           prikaziPodatkeArtikla(kontroler.KontrolerKlijent.getInstance().getSelektovanArtikal());
           this.setTitle("Prikaz podataka artikla");
        }
    }

    private void popuniComboBoxove() {
        try {
            List<OpstiDomenskiObjekat> lista1 = kontroler.KontrolerKlijent.getInstance().vratiListuTipovaArtikala();
            popuniComboTipoviArtikla(lista1);
        } catch (Exception e) {
            prikaziPorukuGreske("Doslo je do problema prilikom popunjavanja combo boxova!");
        }
    }


